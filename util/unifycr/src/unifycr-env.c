#ifndef _CONFIG_H
#define _CONFIG_H
#include <config.h>
#endif

#include <stdio.h>
#include <stdlib.h>

#include "unifycr.h"

int unifycr_read_env(unifycr_env_t *env)
{
    char *val = NULL;

    val = getenv("UNIFYCR_MT");
    if (val)
        env->unifycr_mt = strdup(val);

    return 0;
}

